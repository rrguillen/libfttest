#include "libft.h"

int		ft_space_len(long num)
{
	int i;

	i = 1;
	if (num < 0)
	{
		i++;
		num = -num;
	}
	while (num >= 10)
	{
		i++;
		num /= 10;
	}
	return (i);
}
